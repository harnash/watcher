package cmd

import (
	"log"
	"os"

	"github.com/harnash/watcher/config"
	"github.com/spf13/cobra"
)

var cfgFile string

// RootCmd represents the base command when called without any subcommands
var RootCmd = &cobra.Command{
	Use:   "watcher",
	Short: "Service dashboard for Marathon services.",
	Long: `Server providing basic information about running services with
metrics, healthchecks and other meta informations`,
}

// Execute adds all child commands to the root command sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	if err := RootCmd.Execute(); err != nil {
		log.Fatal(err)
		os.Exit(-1)
	}
}

func init() {
	cobra.OnInitialize(initConfig)

	RootCmd.PersistentFlags().StringVar(&cfgFile, "config", "", "config file (default is $HOME/.watcher.yaml|json|toml)")
}

// initConfig reads in config file and ENV variables if set.
func initConfig() {
	cfg, err := config.Load(cfgFile)
	if err != nil {
		log.Fatalf("[FATAL] Error loading configuration: %s", err)
		os.Exit(3)
	}
	config.AppConfig = *cfg
}
